import { existsSync, readdirSync, readFileSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

/** Inputs for {@link buildEmbedSource}. */
export interface EmbedCodegenInput {
  /** Absolute path to the built web client directory (`apps/web/dist`). */
  distDir: string;
  /** Absolute path to the Drizzle migrations directory (`apps/server/drizzle`). */
  drizzleDir: string;
  /** Absolute path to the directory the generated module will be written to. */
  outDir: string;
}

/** One journal entry shape we read from `meta/_journal.json`. */
interface JournalEntry {
  tag: string;
  when: number;
  breakpoints: boolean;
}

/** Recursively list files under `dir`, returning absolute paths. */
function walk(dir: string): string[] {
  const out: string[] = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full));
    else out.push(full);
  }
  return out;
}

/** Convert an OS path between two locations to a forward-slash import specifier. */
function importSpecifier(fromDir: string, toFile: string): string {
  return relative(fromDir, toFile).split(sep).join('/');
}

/**
 * Build the JavaScript source of the generated embedded module: `with { type: 'file' }` imports
 * for every web-client asset plus an exact URL-to-file manifest, and inlined migration SQL with
 * journal metadata. Emits plain JS (no type annotations) so `tsc` never typechecks it; types
 * come from the committed `embedded.d.ts`. Throws if the web build is missing so a stale or
 * absent build can never yield a half-working binary.
 *
 * Paths and SQL are emitted via `JSON.stringify` so any special characters are escaped into
 * valid JS string literals rather than via hand-rolled quoting.
 *
 * @param input - Dist, Drizzle, and output directories.
 * @returns The module source to write to `generated/embedded.js`.
 * @throws If the web build (index.html) is missing from `distDir`.
 */
export function buildEmbedSource(input: EmbedCodegenInput): string {
  const indexHtml = join(input.distDir, 'index.html');
  if (!existsSync(indexHtml)) {
    throw new Error(`Web build missing: ${indexHtml}. Run "bun run build:web" first.`);
  }

  const files = walk(input.distDir).toSorted();
  const imports: string[] = [];
  const entries: string[] = [];
  let indexVar = '';
  files.forEach((file, i) => {
    const v = `_a${i}`;
    const specifier = JSON.stringify(importSpecifier(input.outDir, file));
    imports.push(`import ${v} from ${specifier} with { type: 'file' };`);
    const urlPath = `/${relative(input.distDir, file).split(sep).join('/')}`;
    entries.push(`  ${JSON.stringify(urlPath)}: ${v},`);
    if (urlPath === '/index.html') indexVar = v;
  });
  if (indexVar === '') throw new Error('index.html missing from dist file list');

  const journalPath = join(input.drizzleDir, 'meta', '_journal.json');
  const journal = JSON.parse(readFileSync(journalPath, 'utf8')) as { entries: JournalEntry[] };
  const migrations = journal.entries.map((e) => {
    const sqlText = readFileSync(join(input.drizzleDir, `${e.tag}.sql`), 'utf8');
    return `  { tag: ${JSON.stringify(e.tag)}, when: ${e.when}, breakpoints: ${e.breakpoints}, sql: ${JSON.stringify(sqlText)} },`;
  });

  return [
    '// GENERATED by tooling/generate-embed.ts - do not edit. Gitignored build artifact.',
    ...imports,
    '',
    `export const WEB_INDEX = ${indexVar};`,
    'export const WEB_ASSETS = {',
    ...entries,
    '};',
    '',
    'export const MIGRATIONS = [',
    ...migrations,
    '];',
    '',
  ].join('\n');
}
